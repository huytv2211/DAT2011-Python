# Bài 1: In thông tin cá nhân
# Viết một chương trình Python để in thông tin cá nhân của bạn, bao gồm:
# - Tên
# - Năm sinh
# - Trường học
# - Quê quán
# Mỗi thông tin in trên một dòng riêng biệt.
# Ví dụ:
print("Xin chào!")
print("Tôi tên là Trần Van Huy")
print("Tôi sinh năm 2005.")
print("Tôi học tại trường Đại học Sư phạm Kỹ thuật TP.HCM.")
print("Tôi đến từ HCM.")

# Kết quả đầu ra:
# Xin chào!
# Tôi tên là Trần Van Huy
# Tôi sinh năm 2005.
# Tôi học tại trường Đại học Sư phạm Kỹ thuật TP.HCM.
# Tôi đến từ HCM.
