# đó tính và in ra chu vi và diện tích của hình vuông đó.
# Công thức:
# Chu vi = 4 * cạnh
# Diện tích = cạnh * cạnh
# Ví dụ:
# Nhập cạnh: 5
# Hình vuông có cạnh = 5.0
# Chu vi = 20.0
