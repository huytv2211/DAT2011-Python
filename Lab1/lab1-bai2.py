# Bài 2: Chu vi và diện tích hình vuông

canh = float(input("Nhập cạnh: "))
chu_vi = canh * 4
dien_tich = canh * canh
print("Hình vuông có cạnh =", canh)
print("Chu vi =", chu_vi)
print("Diện tích =", dien_tich)
